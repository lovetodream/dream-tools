/// Adds control character conveniences to `Byte`.
extension Byte {
    /// +
    static let plus: Byte = 0x2B

    /// -
    static let hyphen: Byte = 0x2D

    /// /
    static let forwardSlash: Byte = 0x2F

    /// =
    static let equals: Byte = 0x3D

    /// _
    static let underscore: Byte = 0x5F
}
